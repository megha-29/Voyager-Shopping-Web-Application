    public static boolean userAuth(String email, String pass) throws NoSuchAlgorithmException {
        User u = selectUser(email);
        if (u != null) {
            String generatedPass = PasswordUtility.hashPassword(pass + u.getSalt());
            if (u.getPassword().equals(generatedPass)) {
                return true;
            }
        }

        return false;
    }
